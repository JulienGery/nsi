void setup() {
  Serial.begin(115200);
  for(auto &i : Ports){
    pinMode(i, OUTPUT);
  }

}

void loop() {
  String message = "hello";
  for(auto &i: message){
    for(auto &j: morse){
      if(j[0] == String(i)){
        for(auto &h : j[1]){
          digitalWrite(2, HIGH);
          h == '-'? delay(1000): delay(100);
          digitalWrite(2, LOW);
        }
      }
    }
  }
}